package com.golfie.auth.infrastructure.naver;

public class NaverAccount {
    private String id;
    private String email;
    private String age;
    private String gender;

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }
}
